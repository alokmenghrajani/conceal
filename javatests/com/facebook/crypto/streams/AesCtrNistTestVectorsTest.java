  private void test(String[] data) throws Exception {
    for (int i = 0; i < data.length; ) {
      Key key = new SecretKeySpec(byteArrFromHex(data[i++]), "AES");
      AlgorithmParameterSpec iv = new IvParameterSpec(byteArrFromHex(data[i++]));
      byte[] plaintext = byteArrFromHex(data[i++]);
      byte[] expectedCipherText = byteArrFromHex(data[i++]);

      // Check that encrypting plaintext with key results in the expected ciphertext
      Cipher cipher = Cipher.getInstance(CIPHER_ALG);
      cipher.init(Cipher.ENCRYPT_MODE, key, iv);
      byte[] cipherText = cipher.update(plaintext, 0, plaintext.length);
      cipher.doFinal();
      Assert.assertArrayEquals(expectedCipherText, cipherText);

      // Check that decrypting cipherText gives us back plaintext
      ByteArrayInputStream input = new ByteArrayInputStream(cipherText);
      cipher.init(Cipher.DECRYPT_MODE, key, iv);
      BetterCipherInputStream cipherStream = new BetterCipherInputStream(input, cipher);
      byte[] decryptedBytes = ByteStreams.toByteArray(cipherStream);
      Assert.assertArrayEquals(plaintext, decryptedBytes);

      break;
    }
  }

  private static byte[] byteArrFromHex(String s) {
    int len = s.length();
    byte[] data = new byte[len / 2];
    for (int i = 0; i < len; i += 2) {
      data[i / 2] = (byte) ((Character.digit(s.charAt(i), 16) << 4)
          + Character.digit(s.charAt(i + 1), 16));
    }
    return data;
  }
